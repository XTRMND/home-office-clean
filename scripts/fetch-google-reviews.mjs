/**
 * One-off: pull real Google reviews for Home Office Clean and write them
 * into src/data/testimonials.ts + public/reviews/*.png.
 *
 * Run: GOOGLE_PLACES_API_KEY='...' node scripts/fetch-google-reviews.mjs
 *
 * Uses the Places API (New). Fetches the same reviews twice (languageCode=bg
 * and =en) and merges by (author, publishTime) so each card has BG and EN
 * variants. All reviews returned by the API are kept (Places API caps the
 * response at ~5 reviews regardless of the total userRatingCount).
 */
import {writeFile, mkdir} from 'node:fs/promises';
import {createHash} from 'node:crypto';
import {join, dirname} from 'node:path';
import {fileURLToPath} from 'node:url';

const repoRoot = join(dirname(fileURLToPath(import.meta.url)), '..');
const dataPath = join(repoRoot, 'src', 'data', 'testimonials.ts');
const reviewsDir = join(repoRoot, 'public', 'reviews');

const KEY = process.env.GOOGLE_PLACES_API_KEY;
if (!KEY) {
  console.error('Missing GOOGLE_PLACES_API_KEY env var.');
  process.exit(1);
}

// The Hox Google Business Profile is not API-discoverable through Text Search,
// so we hardcode the Place ID derived from the Maps URL's hex Feature ID
// (0xac4bf725ca2b3771:0xceed8156c209bef4 → ChIJcTcryiX3S6wR9L4JwlaB7c4).
const PLACE_ID = 'ChIJcTcryiX3S6wR9L4JwlaB7c4';
const GOOGLE_CID_URL = 'https://www.google.com/maps?cid=14910716150916169460';

async function fetchPlaceDetails(placeId, languageCode) {
  const url = `https://places.googleapis.com/v1/places/${placeId}?languageCode=${languageCode}`;
  const res = await fetch(url, {
    headers: {
      'X-Goog-Api-Key': KEY,
      'X-Goog-FieldMask': 'id,displayName,rating,userRatingCount,reviews',
    },
  });
  if (!res.ok) {
    throw new Error(`places/${placeId} (${languageCode}) ${res.status}: ${await res.text()}`);
  }
  return res.json();
}

function reviewKey(r) {
  return `${r.authorAttribution?.displayName ?? ''}::${r.publishTime ?? ''}`;
}

function formatDate(iso, locale) {
  const d = new Date(iso);
  return new Intl.DateTimeFormat(locale === 'bg' ? 'bg-BG' : 'en-US', {
    year: 'numeric',
    month: 'long',
  }).format(d);
}

async function downloadAvatar(photoUri, filename) {
  // Author photos are direct googleusercontent.com URLs. Force a small size
  // by replacing the trailing size segment (=s128-c, =s64-c, etc.) with =s96.
  const sized = photoUri.replace(/=s\d+(-c)?$/, '=s96-c');
  const res = await fetch(sized);
  if (!res.ok) throw new Error(`photo ${res.status} ${sized}`);
  const buf = Buffer.from(await res.arrayBuffer());
  const path = join(reviewsDir, filename);
  await writeFile(path, buf);
  return buf.length;
}

function tsFileContents({aggregate, records}) {
  return `// Real Google reviews for Хоум Офис Клийн / Home Office Clean.
// Source: ${GOOGLE_CID_URL}
//
// Generated by scripts/fetch-google-reviews.mjs — re-run that script to refresh.

export type Testimonial = {
  name: string;
  rating: 1 | 2 | 3 | 4 | 5;
  date: string;
  text: string;
  lang: 'bg' | 'en';
  avatar?: string;
};

export const googleAggregate = ${JSON.stringify(aggregate, null, 2)} as const;

export const testimonials: readonly Testimonial[] = ${JSON.stringify(records, null, 2)};
`;
}

async function main() {
  const [bgDetails, enDetails] = await Promise.all([
    fetchPlaceDetails(PLACE_ID, 'bg'),
    fetchPlaceDetails(PLACE_ID, 'en'),
  ]);

  console.log(`✓ ${bgDetails.displayName?.text} (${PLACE_ID})`);

  console.log(
    `✓ Aggregate: ${bgDetails.rating} ★ (${bgDetails.userRatingCount} reviews total)`,
  );

  const bgReviews = bgDetails.reviews ?? [];
  const enReviews = enDetails.reviews ?? [];
  console.log(`✓ Reviews returned: ${bgReviews.length} (bg), ${enReviews.length} (en)`);

  const enByKey = new Map(enReviews.map((r) => [reviewKey(r), r]));

  await mkdir(reviewsDir, {recursive: true});

  const records = [];
  const avatarStats = [];

  for (const bg of bgReviews) {
    const en = enByKey.get(reviewKey(bg));
    const author = bg.authorAttribution?.displayName ?? 'Anonymous';
    const photoUri = bg.authorAttribution?.photoUri;

    let avatarPath;
    if (photoUri) {
      const hash = createHash('sha256')
        .update(`${author}::${bg.publishTime}`)
        .digest('hex')
        .slice(0, 12);
      const filename = `${hash}.png`;
      try {
        const bytes = await downloadAvatar(photoUri, filename);
        avatarPath = `/reviews/${filename}`;
        avatarStats.push(`${filename} (${bytes}B)`);
      } catch (err) {
        console.warn(`  ! avatar for ${author}: ${err.message}`);
      }
    }

    const bgText = bg.text?.text ?? bg.originalText?.text ?? '';
    const enText = en?.text?.text ?? en?.originalText?.text ?? bgText;

    const rating = bg.rating ?? en?.rating ?? 5;

    records.push({
      name: author,
      rating,
      date: formatDate(bg.publishTime, 'bg'),
      text: bgText,
      lang: 'bg',
      ...(avatarPath ? {avatar: avatarPath} : {}),
    });
    records.push({
      name: author,
      rating,
      date: formatDate(bg.publishTime, 'en'),
      text: enText,
      lang: 'en',
      ...(avatarPath ? {avatar: avatarPath} : {}),
    });
  }

  console.log(`✓ Avatars downloaded: ${avatarStats.length}`);
  avatarStats.forEach((s) => console.log(`  · ${s}`));

  const aggregate = {
    rating: Number(bgDetails.rating.toFixed(1)),
    count: bgDetails.userRatingCount,
    url: GOOGLE_CID_URL,
  };

  await writeFile(dataPath, tsFileContents({aggregate, records}));
  console.log(`✓ Wrote ${dataPath} (${records.length} entries — ${records.length / 2} reviews × 2 langs)`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
